#Get the character at position
a = "Hello, World!"
print(a[1])

#Slicing
b = "Hello, World!"
print(b[2:3])

#Negative Indexing
c = "Hello, World!"
print(c[-5:-2])

#String Length
d = "Hello, World!"
print(len(d))

#String Methods
e = " Hello, World! "
print(e.strip())

g = "Hello, World!"
print(g.lower())

h = "Hello, World!"
print(h.upper())

